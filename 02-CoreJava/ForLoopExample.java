
public class ForLoopExample {

    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println("Iteration: " + i);
        }
    }
}

/*
Output:
    Iteration: 1
    Iteration: 2
    Iteration: 3
    Iteration: 4
    Iteration: 5

Explanation:
    1. The for loop is used to execute a block of code multiple times.
    2. The loop consists of three parts: initialization, condition, and increment/decrement.
    3. In the example, we use a for loop to print the message "Iteration: " followed by the loop variable i.
    4. The loop variable i is initialized to 1, and the loop continues as long as i is less than or equal to 5.
    5. After each iteration, the loop variable i is incremented by 1.
    6. The loop executes 5 times, printing the message for each iteration.
*/
