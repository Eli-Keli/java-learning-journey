
public class DataTypesExample {

    public static void main(String[] args) {
        int age = 22; // Integer (whole number)
        double height = 5.9; // Decimal number
        char grade = 'A'; // Single character
        boolean isStudent = true; // True/False
        String name = "Eli"; // Text (Non-primitive)

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
        System.out.println("Grade: " + grade);
        System.out.println("Is a student? " + isStudent);
    }
}

// Output:
//  Name: Eli
//  Age: 22
//  Height: 5.9
//  Grade: A
//  Is a student? true

// Explanation: In this program, we declare and initialize variables of different data types. We use int for whole numbers, double for decimal numbers, char for single characters, boolean for true/false values, and String for text (non-primitive data type). We then print the values of these variables using the System.out.println() method. The output displays the values of the variables along with their corresponding data types.
